// Task 3: Name Cases: Store a person’s name in a variable,
// and then print that person’s name in lowercase, uppercase, and titlecase.

let x = "mudasir"
// Lowercase
console.log("lowercase : ",  x . toLowerCase());
// Uppercase
console.log("uppercase : ", x . toUpperCase())
// Titlecase 
console.log("Titlecase :", x .charAt(0).toUpperCase() + x .slice(1).toLowerCase());
